export default function ServicesPage() {
  const searchParams = useSearchParams();
  const initialTab = searchParams.get('type') === 'business' ? 'business' : 'individual';
  const [tab, setTab] = useState<'individual' | 'business'>(initialTab);
  const [showForm, setShowForm] = useState(false);
  const [selectedServiceName, setSelectedServiceName] = useState<string | null>(null);

  const services = tab === 'individual' ? individualServices : businessServices;
  const categories = Array.from(new Set(services.map(s => s.category)));

  const openForm = (name: string) => {
    setSelectedServiceName(name);
    setShowForm(true);
  };

  return (
    <div className={styles.servicesPage}>
      <div className={styles.tabs}>
        <button className={tab === 'individual' ? styles.active : ''} onClick={() => setTab('individual')}>Физические лица</button>
        <button className={tab === 'business' ? styles.active : ''} onClick={() => setTab('business')}>Юридические лица</button>
      </div>

      {categories.map(category => (
        <div key={category} className={styles.categorySection}>
          <h2>{category}</h2>
          <div className={styles.servicesList}>
            {services.filter(s => s.category === category).map((service, index) => (
              <div key={index} className={styles.serviceCard}>
                <h3>{service.title}</h3>
                <p>{service.description}</p>
                <p>
                  <strong><img src="/clock.svg" alt="clock" style={{ position: 'relative', top: '5px', marginRight: '8px' }} />Продолжительность:</strong> {service.duration}
                </p>
                <p>
                  <strong><img src="/Group.svg" alt="group" style={{ position: 'relative', top: '5px', marginRight: '8px' }} />Стоимость:</strong> {service.price}
                </p>
                <button className={styles.signupButton} onClick={() => openForm(service.title)}>Записаться</button>
              </div>
            ))}
          </div>
        </div>
      ))}

      {showForm && selectedServiceName !== null && (
        <RequestForm
          onClose={() => setShowForm(false)}
          defaultServiceName={selectedServiceName}
        />
      )}
    </div>
  );
}
